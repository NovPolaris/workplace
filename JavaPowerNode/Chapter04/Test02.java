public class Test02 {
	public static void main(String[] args) {
		int a = 15;
		int b = 2;
		double c = 2;
		System.out.println(a + "/" + b + "=" + (a/b)); // 15/2=7
		System.out.println(a + "%" + b + "=" + (a%b)); // 15%2=1
		System.out.println(a + "/" + c + "=" + (a/c)); // 15/2=7.5
		System.out.println(a + "%" + c + "=" + (a%c)); // 15%2=1.0
	}
}